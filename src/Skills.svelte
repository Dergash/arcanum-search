<h2>Skills</h2>
<h3>Skill Level</h3>
    <p>
        All Arcanum creatures could have skills in range of 0 - 20
        Skill level is derived from:
    </p>
    <ul>
        <li>skill points invested (4 levels for each skill point)</li>
        <li>race modifiers</li>
        <li>equipped items</li>
        <li>spells and potions</li>
        <li>background modifiers (player and followers only)</li>
        <li>blessings (player only)</li>
    </ul>
<h3>Training</h3>
    <p>
        Training is a rank which could be obtained from teacher for money or after specific quest.
        There are four training ranks:
    </p>
    <ul>
        <li>Untrained</li>
        <li>Apprentice (or Beginner in WorldEd)</li>
        <li>Expert</li>
        <li>Master</li>
    </ul>
    